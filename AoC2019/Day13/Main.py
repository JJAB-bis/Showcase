from AoC2019.IntComp import IntComp

def Timer(func):
    def Timer_wrapper(*arg, rtrn:bool=True, prnt:bool=False, **args):
        """ rtrn: return the result if true
            prnt: print -- {the function that is being timed}: {result} -- {time it took}
            *arg and **args contain unnamed and named variables respectably to be passed to the function to be timed"""
        t0 = time()
        out = func(*arg, **args)
        if prnt == True:
            print( f"{str(func.__name__)}: {out}  -- {time()-t0}" )
        if rtrn:
            return out
    return Timer_wrapper

@ Timer
def parse(test:int, *arg, **args):
    r""" imports input file from "AoC2020\Day{day}\{f}"  """
    
    path = '\\'.join( os.path.abspath(__file__).split('\\')[:-1] )
    file_loc = f"""{path}\\{f"{f'test_{test}_' if test > 0 else ''}input.txt"}"""
    with open(file_loc) as f:
        comp = IntComp(f)
    return comp

class Arcade:
    def __init__(self, comp, update_len = 6):
        comp.input_program[0] = 2
        process = comp.run(status_updates=True)
        next(process)
        self.comp    = comp
        self.process = process
        self.tile_lookup   = { 0: '.',1: '#',2: '$',3: '_',4: 'X'}
        self.allowed_input = (-1,0,1)
        self.update_len    = update_len
        self.frames        = 0
        field = self.comp.output
        instr_len = 3
        self.field = { 
                (field[i+0],field[i+1]) : field[i+2] 
                for i in range(0,len(field), instr_len)
            }
        self.width,self.height = 0, 0
        for x,y in self.field.keys():
            self.width = max(self.width, x)
            self.height = max(self.height, y)
        self._get_paddle_and_ball()
        print(self)

    def _get_paddle_and_ball(self):
        for (x,y), i in self.field.items():
            if i == 3:
                self.paddle     = [x,y]
            elif i == 4:
                self.ball       = (x,y)
                self.ball_prev  = (x,y)


    def run(self, mode='a', instr = None):
        mode = 'm' if instr is None else mode
        instr = [] if instr is None else instr
        self._get_paddle_and_ball()
        self.frames = 0
        while self.comp.running:
            inp = None if mode == 'm' or self.frames >= len(instr) else instr[self.frames]
            if self.frames >= len(instr): print(self)
            if inp is None:
                while True:
                    try:
                        inp = int(input())
                        assert(inp in self.allowed_input)
                        instr.append(inp)
                        break
                    except (ValueError,AssertionError) as e:
                        print( e )
                        print(f"Input must be {self.allowed_input}")
            self.update(inp)

        print(self)
        print(instr)

        with open('AoC2019\Day13\instruction.txt', 'w') as f:
            f.write(str(instr ) )

    def update(self, inp):
        # send input
        self.process.send(inp)
        
        # update ball 
        self.field[tuple(self.paddle)] = 0
        self.paddle[0] += inp
        self.field[tuple(self.paddle)] = 3
        
        
        self.frames += 1
        instr = self.comp.get_out(self.update_len)
        self.ball_prev = self.ball
        self.field[tuple(self.ball)] = 0
        self.ball = instr[:2]
        self.field[tuple(self.ball)] = 4

    
    def __str__(self):
        s = ''
        for y in range(self.height+1):
            for x in range(self.width+1):
                if (x,y) == self.ball_prev and self.frames > 0:
                    s += 'o'
                else:
                    s += self.tile_lookup[self.field[(x,y)]]
            s += '\n'
        return s






